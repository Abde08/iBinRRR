package com.snowcrash.greenheart.model;

public class CollectionState {
    private String code;
    private String name;

    public CollectionState() {}

    public CollectionState(String code, String name) {
        this.code = code;
        this.name = name;
    }

    // Getters and setters
    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

}

